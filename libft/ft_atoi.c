#include "libft.h"

int	ft_atoi(const char *str)
{
	int long i;
	int long j;

	i = 1;
	j = 0;
	while ((*str == '\n') || (*str == '\t') || (*str == '\v')
			|| (*str == ' ') || (*str == '\f') || (*str == '\r'))
		str++;
	if (*str == '-')
		i = -1;
	if (*str == '-' || *str == '+')
		str++;
	while (*str >= '0' && *str <= '9' && *str != '\0')
	{
		j = j * 10 + (*str - '0');
		str++;
		if (j > 100000000000000000 && i == 1)
			return (-1);
		if (j > 100000000000000000 && i == -1)
			return (0);
	}
	return (j * i);
}
