#include "ft_printf.h"

int	ft_atoi_length(int n)
{
	size_t	i;

	if (n > 0)
		i = 0;
	else
		i = 1;
	while (1)
	{
		i++;
		n /= 10;
		if (!n)
			break ;
	}
	return (i);
}
